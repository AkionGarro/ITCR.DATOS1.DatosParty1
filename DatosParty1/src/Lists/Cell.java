package Lists;

public class Cell {
    
    int cellNumber;
    int xPosition;
    int yPosition;
    String color;
    String action;

    public Cell(int cellNumber, int xPosition, int yPosition, String color, String action) {
        
        this.cellNumber = cellNumber;
        this.xPosition = xPosition;
        this.yPosition = yPosition;
        this.color = color;
        this.action = action;
        
    }
    
}
